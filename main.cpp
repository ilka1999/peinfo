int main(int argc,char *argv[])
  {
   CPEInfo peinfo;

   wchar_t path[MAX_PATH];
   ::MultiByteToWideChar(CP_ACP,0,argv[1],-1,path,MAX_PATH);

   ::wprintf(L"%s\n",path);
   peinfo.Open(path);

   return(0);
  }

